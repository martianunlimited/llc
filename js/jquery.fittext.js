(function( $ ){

  $.fn.fitText = function( kompressor, options ) {

    // Setup options
    var compressor = kompressor || 1,
        settings = $.extend({
          'minFontSize' : Number.NEGATIVE_INFINITY,
          'maxFontSize' : Number.POSITIVE_INFINITY
        }, options);

    return this.each(function(){

      // Store the object
      var $this = $(this);
      var wheight = $(window).height();
      var wwidth = $(window).width(); 
      var ratio = Math.min(wwidth/800,1);
      var ratio2 = Math.max(wheight/800,1);
      var ratio3 = Math.max(wwidth/wheight,1);
        
        console.log('Ratio: '+ ratio +' Ratio2: '+ ratio2 + ' Ratio3: '+ratio3);
      // Resizer() resizes items based on the object width divided by the compressor * 10
      var resizer = function () {
        $this.css('font-size', Math.max(Math.min($this.width()*ratio2 / (compressor*10*ratio3/ratio), parseFloat(settings.maxFontSize)), parseFloat(settings.minFontSize)));
      };

      // Call once to set.
      resizer();

      // Call on resize. Opera debounces their resize by default.
      $(window).on('resize.fittext orientationchange.fittext', resizer);

    });

  };

})( jQuery );
